#include<unistd.h>

int	main(int argc, char **argv)
{
	int	j;
	int	i;

	i = 0;
	j = argc -1;
	while (1 <= j)
	{
		while (argv[j][i])
		{
			write (1, &argv[j][i], 1);
			i++;
		}
		write (1, "\n", 1);
		i = 0;
		j--;
	}
}
